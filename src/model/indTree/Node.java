package model.indTree;

import java.io.Serializable;
import java.util.ArrayList;

import javax.swing.tree.DefaultMutableTreeNode;

/**
 * Ova klasa predstavlja jedan cvor stabla
 * 
 * @author Lenka
 *
 */
public class Node extends DefaultMutableTreeNode implements Serializable {

	public ArrayList<NodeElement> data;
	public ArrayList<Node> children;

	public Node() {
		data = new ArrayList<NodeElement>();
		children = new ArrayList<Node>();
	}

	public ArrayList<NodeElement> getData() {
		return data;
	}

	public ArrayList<Node> getChildren() {
		return children;
	}

	
}
